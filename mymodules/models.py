class Student:
    def __init__(self, name="", student_grade=0):
        self.name = name
        self.student_grade = student_grade
    def set_grade(grade):
        self.student_grade = grade
    def get_grade(self):
        return self.student_grade
    def print_student_info(self):
        print(self.name + ": " + str(self.student_grade))
